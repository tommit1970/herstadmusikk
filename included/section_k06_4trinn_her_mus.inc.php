        <h4>Musisere</h4>
		M&#229;l for oppl&#230;ringen er at eleven skal kunne
		<ul>
            <li>holde en jevn puls i ulike tempi</li>
            <li>imitere og improvisere over enkle rytmer og klanger</li>
            <li>spille enkle ostinater og melodier etter geh&#248;r</li>
            <li>beherske et sangrepertoar fra ulike sjangere og synge med fokus p&#229; intonasjon</li>
            <li>danse et utvalg norske og internasjonale folkedanser</li>
            <li>framf&#248;re sang, spill og dans i samhandling med andre</li>
        </ul>
		
        <h4>Komponere</h4>
		M&#229;l for oppl&#230;ringen er at eleven skal kunne
		<ul>
            <li>eksperimentere med sang, talekor og instrumenter i enkle samspill</li>
            <li>komponere melodier og lydillustrasjoner til tekster og lage egne tekster til musikk</li>
            <li>improvisere dans og bevegelse og samtale om hvordan dans kan illustrere et musikalsk forl&#248;p</li>
        </ul>

        <h4>Lytte</h4>
		M&#229;l for oppl&#230;ringen er at eleven skal kunne
		<ul>
			<li>gjenkjenne og beskrive klang, melodi, rytme, dynamikk, tempo og form</li>
			<li>gjenkjenne norsk og samisk folkemusikk og folkemusikk fra andre kulturer</li>
			<li>delta i samtaler om hva som er s&#230;regne trekk ved enkelte musikksjangere</li>
			<li>gjenkjenne lyden av og navngi sentrale bl&#229;se-, strenge- og slagverkinstrumenter</li>
			<li>gj&#248;re rede for egne musikkopplevelser, om ulik bruk av musikk og ulike funksjoner musikk kan ha</li>
        </ul>		
		
